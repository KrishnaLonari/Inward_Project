package com.api.remitGuru.component.util;

public class FTPBean {

	private String ftpServerName;
	private String ftpPath;
	private String ftpServerUsername;
	private String ftpServerPassword;
	private String ftpUploadedFilePath;
	private String ftpFileToDownloadPath;	
	private String ftpDownloadedFilePath;	
		
	/**
	 * @return the ftpServerName
	 */
	public String getFtpServerName() {
		return ftpServerName;
	}



	/**
	 * @param ftpServerName the ftpServerName to set
	 */
	public void setFtpServerName(String ftpServerName) {
		this.ftpServerName = ftpServerName;
	}



	/**
	 * @return the ftpPath
	 */
	public String getFtpPath() {
		return ftpPath;
	}



	/**
	 * @param ftpPath the ftpPath to set
	 */
	public void setFtpPath(String ftpPath) {
		this.ftpPath = ftpPath;
	}



	/**
	 * @return the ftpServerUsername
	 */
	public String getFtpServerUsername() {
		return ftpServerUsername;
	}



	/**
	 * @param ftpServerUsername the ftpServerUsername to set
	 */
	public void setFtpServerUsername(String ftpServerUsername) {
		this.ftpServerUsername = ftpServerUsername;
	}



	/**
	 * @return the ftpServerPassword
	 */
	public String getFtpServerPassword() {
		return ftpServerPassword;
	}



	/**
	 * @param ftpServerPassword the ftpServerPassword to set
	 */
	public void setFtpServerPassword(String ftpServerPassword) {
		this.ftpServerPassword = ftpServerPassword;
	}



	/**
	 * @return the ftpUploadedFilePath
	 */
	public String getFtpUploadedFilePath() {
		return ftpUploadedFilePath;
	}



	/**
	 * @param ftpUploadedFilePath the ftpUploadedFilePath to set
	 */
	public void setFtpUploadedFilePath(String ftpUploadedFilePath) {
		this.ftpUploadedFilePath = ftpUploadedFilePath;
	}



	/**
	 * @return the ftpFileToDownloadPath
	 */
	public String getFtpFileToDownloadPath() {
		return ftpFileToDownloadPath;
	}



	/**
	 * @param ftpFileToDownloadPath the ftpFileToDownloadPath to set
	 */
	public void setFtpFileToDownloadPath(String ftpFileToDownloadPath) {
		this.ftpFileToDownloadPath = ftpFileToDownloadPath;
	}



	/**
	 * @return the ftpDownloadedFilePath
	 */
	public String getFtpDownloadedFilePath() {
		return ftpDownloadedFilePath;
	}



	/**
	 * @param ftpDownloadedFilePath the ftpDownloadedFilePath to set
	 */
	public void setFtpDownloadedFilePath(String ftpDownloadedFilePath) {
		this.ftpDownloadedFilePath = ftpDownloadedFilePath;
	}

}
